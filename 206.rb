# 1_2_3_4_5_6_7_8_9_0
# 1000000000000000000
# 1929394959697989990 => 1389026623
# 1020304050607080900 => 1010101010

def valid(n)
  ns = n.to_s
  begin
    return ns[0..0] + ns[2..2] + ns[4..4] + ns[6..6] + ns[8..8] + ns[10..10] + ns[12..12] + ns[14..14] + ns[16..16] + ns[18..18] == "1234567890"
  rescue
    return false
  end
end

n = 1010101030
while !valid(n**2)
  n += 20
end
puts [n, n**2]